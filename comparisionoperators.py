# Comparision Operators in Python

# == (equal to)
# != (not equal to)
# > (greater than)
# < (less than)
# >= (greater than or equal to)
# <= (less than or equal to)
a = 10
b = 20
print("a == b:", a == b)  # False
print("a != b:", a != b)  # True
print("a > b:", a > b)    # False
print("a < b:", a < b)    # True
print("a >= b:", a >= b)  # False
print("a <= b:", a <= b)  # True
# Comparing strings
str1 = "hello"
str2 = "world"
print("str1 == str2:", str1 == str2)  # False
print("str1 != str2:", str1 != str2)  # True
print("str1 > str2:", str1 > str2)    # False
print("str1 < str2:", str1 < str2)    # True
print("str1 >= str2:", str1 >= str2)  # False
print("str1 <= str2:", str1 <= str2)  # True
